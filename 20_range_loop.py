"""
range is a built-in function used to generate a sequence of numbers.
range(stop)
range(start, stop)
range(start, stop, step)
"""

# 0 1 2
for i in range(3):
    print(i)

print('--------------')

# 2 3 4
for i in range(2, 5):
    print(i)

print('--------------')

# 1 3 5 7
for i in range(1, 9, 2):
    print(i)

