"""
loop is used to iterate over a sequence (such as a list, tuple, string, or range)
execute a block of code for each item in the sequence
"""
fruits = ['apple', 'orange', 'mango']
i = 0
while i < len(fruits):
    print(fruits[i])
    i += 1

print('--------------')

count = 0
while count < 3:
    print(count)
    count += 1

print('--------------')

